import { ReactNode, useState } from "react";
import PrettierDate from "./assets/services/PrettierDate/PrettierDate";

type VideoTypeProps = {
  url: string;
  date: string;
};

const PrettyDateTime = PrettierDate(DateTime);

function DateTime(props: { date: string }): ReactNode {
  return <p className="date">{props.date}</p>;
}

function Video(props: VideoTypeProps): ReactNode {
  return (
    <div className="video">
      <iframe
        src={props.url}
        frameBorder="0"
        allow="autoplay; encrypted-media"
        allowFullScreen
      ></iframe>
      <PrettyDateTime date={props.date} />
    </div>
  );
}

function VideoList(props: { list: VideoTypeProps[] }): ReactNode {
  return props.list.map((item) => <Video url={item.url} date={item.date} />);
}

